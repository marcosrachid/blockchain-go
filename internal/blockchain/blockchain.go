var (
	dbPath      = getDBPath()
	genesisData = "First Transaction from Genesis"
)

// getDBPath returns the database path, checking environment variable first
func getDBPath() string {
	if path := os.Getenv("BLOCKCHAIN_DATA_DIR"); path != "" {
		return path + "/blocks"
	}
	return "./tmp/blocks"
}

type Blockchain struct {
	LastHash []byte
	Database *badger.DB
}

// BlockchainIterator iterates over blockchain blocks
type BlockchainIterator struct {
	CurrentHash []byte
	Database    *badger.DB
}

// InitBlockchain initializes a new blockchain with genesis block
func InitBlockchain(address string) *Blockchain {
	var lastHash []byte

	// Create directory if it doesn't exist
	if err := os.MkdirAll(dbPath, os.ModePerm); err != nil {
		Handle(err)
	}

	opts := badger.DefaultOptions(dbPath)
	opts.Dir = dbPath
	opts.ValueDir = dbPath

	db, err := badger.Open(opts)
	Handle(err)

	err = db.Update(func(txn *badger.Txn) error {
		if _, err := txn.Get([]byte("lh")); err == badger.ErrKeyNotFound {
			fmt.Println("No existing blockchain found")
			cbtx := CoinbaseTX(address, genesisData)
			genesis := Genesis(cbtx)
			fmt.Println("Genesis created")
			err = txn.Set(genesis.Hash, genesis.Serialize())
			Handle(err)
			err = txn.Set([]byte("lh"), genesis.Hash)

			lastHash = genesis.Hash

			return err
		} else {
			item, err := txn.Get([]byte("lh"))
			Handle(err)
			err = item.Value(func(val []byte) error {
				lastHash = append([]byte{}, val...)
				return nil
			})
			return err
		}
	})
	Handle(err)

	blockchain := Blockchain{lastHash, db}
	return &blockchain
}

